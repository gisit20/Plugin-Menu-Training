    private void refresh() {
        StringBuffer sql = new StringBuffer();
        StringBuffer filter = new StringBuffer();

//        edit ini
        sql.append("Select saleno, sale.saledate, sale.custid, sale.total FROM sale "
//                + "LEFT JOIN sale On sale.saleno=saled.saledno "
//                + "LEFT JOIN branch br On br.branchid=saled.branchid "
        );
        JBSQL.ANDFilterPeriode(filter, "saledate", jBOSPeriode1);
        JBSQL.ANDFilterPicker(filter, "custid", pikCust1);
//        if (kode != null) {
//            JBSQL.ANDFilter(filter, JBSQL.filterUpperLike("saledno", kode));
//        }

        JBSQL.setWHERE(sql, filter);
        JBSQL.setORDERBY(sql, "saleno, saledate");
        if (qds.isOpen()) {
            qds.close();
        }
        qds.setQuery(new QueryDescriptor(BDM.getDefault().getDatabase(), sql.toString()));
        qds.open();

        initTable();
        if (dsv.isOpen()) {
            dsv.close();
        }
        dsv.setStorageDataSet(qds.getStorageDataSet());
        dsv.open();
    }

